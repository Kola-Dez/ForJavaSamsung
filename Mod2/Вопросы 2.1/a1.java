public class a1 {
    public static void main(String[] args) {
        // Строка, представляющая число в двоичной системе
        String binaryString = "1001101";

        // Преобразование строки в десятичное число
        int x = Integer.parseInt(binaryString, 2);

        // Вывод значения переменной x
        System.out.println(x);
    }
}
